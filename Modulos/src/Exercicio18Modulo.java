public class Exercicio18Modulo {
    public double seno(double angulo) {
        double a = Math.toRadians(angulo);
        return Math.sin(a);
    }

    public double cosseno(double angulo) {
        double a = Math.toRadians(angulo);
        return Math.cos(a);
    }

    public double tangente(double angulo) {
        double a = Math.toRadians(angulo);
        return Math.tan(a);
    }
}
